    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {
        if (transacciones.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay transacciones para guardar.");
            return;
        }
        try (PrintWriter writer = new PrintWriter(new FileWriter("transacciones.txt"))) {
            for (Transaccion t : transacciones) {
                writer.printf("Fecha: %s, Descripci√≥n: %s, Monto: %.2f, Tipo: %s%n",
                        t.getFecha(), t.getDescripcion(), t.getMonto(), t.getTipo());
            }
            JOptionPane.showMessageDialog(this, "Transacciones guardadas exitosamente en: "
                    + new java.io.File("transacciones.txt").getAbsolutePath());
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar las transacciones: " + e.getMessage());
        }
    }

    private void btnAgregarTransActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAgregarTransActionPerformed
        // Aqu√≠ deber√≠as abrir un di√°logo o formulario para ingresar los datos de la
        // nueva transacci√≥n
        String fechaStr = JOptionPane.showInputDialog("Fecha (yyyy-MM-dd):");
        LocalDate fecha = null;
        try {
            fecha = new SimpleDateFormat("yyyy-MM-dd").parse(fechaStr).toInstant()
                    .atZone(java.time.ZoneId.systemDefault()).toLocalDate();
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use yyyy-MM-dd.");
            return;
        }
        String descripcion = JOptionPane.showInputDialog("Descripci√≥n:");
        double monto = 0;
        boolean validInput = false;
        while (!validInput) {
            try {
                monto = Double.parseDouble(JOptionPane.showInputDialog("Monto:"));
                validInput = true;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Monto inv√°lido. Por favor, ingrese un n√∫mero.");
            }
        }
        String tipo = JOptionPane.showInputDialog("Tipo (Ingreso/Salida):");
        while (!tipo.equalsIgnoreCase("Ingreso") && !tipo.equalsIgnoreCase("Salida")) {
            tipo = JOptionPane.showInputDialog("Tipo inv√°lido. Por favor, ingrese 'Ingreso' o 'Salida':");
        }

        Transaccion nuevaTransaccion = new Transaccion(fecha, descripcion, monto, tipo);
        transacciones.add(nuevaTransaccion);
        actualizarSaldo();
        actualizarTabla();
    }// GEN-LAST:event_btnAgregarTransActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnEliminarActionPerformed
        int filaSeleccionada = TbMovimientos.getSelectedRow();
        if (filaSeleccionada >= 0) {
            transacciones.remove(filaSeleccionada);
            actualizarSaldo();
            actualizarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona una transacci√≥n para eliminar.");
        }
    }// GEN-LAST:event_btnEliminarActionPerformed

    private void BtnAplicarActionPerformed(java.awt.event.ActionEvent evt) {
        // Filtra las transacciones seg√∫n las fechas seleccionadas
        Date fechaInicioDate = DcInicio.getDate();
        Date fechaFinDate = DcFin.getDate();

        if (fechaInicioDate == null || fechaFinDate == null) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione ambas fechas de inicio y fin.");
            return;
        }

        LocalDate fechaInicio = fechaInicioDate.toInstant().atZone(java.time.ZoneId.systemDefault()).toLocalDate();
        LocalDate fechaFin = fechaFinDate.toInstant().atZone(java.time.ZoneId.systemDefault()).toLocalDate();

        if (fechaInicio.isAfter(fechaFin)) {
            JOptionPane.showMessageDialog(this, "La fecha de inicio no puede ser posterior a la fecha de fin.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) TbMovimientos.getModel();
        model.setRowCount(0); // Limpia la tabla

        for (Transaccion t : transacciones) {
            LocalDate fechaTransaccion = t.getFecha();
            if ((fechaTransaccion.isEqual(fechaInicio) || fechaTransaccion.isAfter(fechaInicio)) &&
                    (fechaTransaccion.isEqual(fechaFin) || fechaTransaccion.isBefore(fechaFin))) {
                model.addRow(new Object[] { fechaTransaccion, t.getDescripcion(), t.getMonto(), t.getTipo() });
            }
        }
    }// GEN-LAST:event_BtnAplicarActionPerformed

