    private void actualizarSaldo() {
        saldo = transacciones.stream()
                .mapToDouble(t -> t.getTipo().equalsIgnoreCase("Ingreso") ? t.getMonto() : -t.getMonto()).sum();
        txtMonto.setText(String.format("%.2f", saldo));
    }

    private void actualizarTabla() {
        // Actualiza la tabla con las transacciones
        DefaultTableModel model = (DefaultTableModel) TbMovimientos.getModel();
        model.setRowCount(0); // Limpia la tabla
        for (Transaccion t : transacciones) {
            model.addRow(new Object[] { t.getFecha(), t.getDescripcion(), t.getMonto(), t.getTipo() });
        }
    }

